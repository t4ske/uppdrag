      <h2>Uppdrag</h2>
      <p>Skapa en responsiv webbplats med följande innehåll:</p>
      <ul class="listmargin">
          <li>En <span class="fontweightnormal">Om-Mig-sida</span> där du med bild och text (ca 15 - 50 meingar) beskriver vem du är.</li>
          <li>En <span class="fontweightnormal">varför-sida</span> där du skriver korta meingar, typ kloterplank, som beskriver varför du vill lära dig det här. (Du bygger nämligen webbplatsen för att repetera och lära dig...)</li>
          <li>En <span class="fontweightnormal">HTML &amp; CSS-sida</span> där du beskriver allt du kan om HTML och CSS. Vi kommer att utgå ifrån boken, beta av kapitel för kapitel, och du fyller på eftersom.</li>
          <li>En <span class="fontweightnormal">guide-sida</span> där du beskriver ALL kodning (rad för rad) som använts för att bygga ramverket till denna webbplats. Det blir html, css och PHP alltså. Du skall skriva så att en sextonåring klart och tydligt kan förstå och tillverka en likadan webbplats efter dina anvisningar.</li>
      </ul>
  </article>

  


